    private boolean comprobarCredenciales() {

        //Recorrer la lista de usuarios para ver que el usuario exista y pueda ingresar al sistema
        boolean credencialesCorrectas = false;
        mEsVendedor = false;
        mEsAdmin = false;
        mEsAcomodador= false;

        Iterator<Usuario> iterador = mListaDeUsuarios.iterator();

        while (iterador.hasNext()) {

            Usuario tmpUsuario = iterador.next();

            //   Acomodador             .equals (mUsuario) &&   Acomodador             .equals (mPass)
            if (tmpUsuario.getNombre().equals(mUsuario) && tmpUsuario.getPass().equals(mPass)) {

                Iterator<String> iteradorRol = tmpUsuario.getListaRoles().iterator();

                while (iteradorRol.hasNext()) {

                    String tmpRol = iteradorRol.next();

                    if (tmpRol.equals("Administrador")) {

                        mEsAdmin = true;

                    } else if (tmpRol.equals("Vendedor")) {

                        mEsVendedor = true;
                    }else if (tmpRol.equals("Acomodador")){
                        
                        mEsAcomodador = true;
                        
                    }
                }

                credencialesCorrectas = true;
                break;

            }
        }

        return credencialesCorrectas;

    }

    private void obtenerCredenciales() {

        //Obtener usuario y contrase침a
        mUsuario = txtUsuario.getText();
        mPass = new String(txtPass.getPassword());
    }

